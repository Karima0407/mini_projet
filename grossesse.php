       <h1>Info Grossesse</h1>
    <h4>La grossesse est une période de changements rapides pour votre corps. La plupart des changements sont temporaires et disparaîtront graduellement après l’accouchement. Certains peuvent être agréables, tandis que d’autres peuvent vous causer des désagréments.</h4>
    <h1>Cœur, vaisseaux sanguins et poumons</h1>
    <h4>Pendant la grossesse, votre pouls peut augmenter d’environ 10 battements par minute. Votre cœur se déplace légèrement dans votre cage thoracique à mesure que le bébé grandit et que l’utérus prend de l’expansion. Le volume de votre sang augmente pour combler les besoins du fœtus.

        La grossesse entraîne des changements qui ont aussi un effet sur la respiration. Plusieurs femmes enceintes se sentent légèrement essoufflées pendant leur grossesse. Cet essoufflement s’installe progressivement et demeure léger. Il peut être présent dès les premiers mois de la grossesse.</h4>
    <h1>Cheveux et poils</h1>
    <h4>La pousse des cheveux et des poils peut être modifiée. Pendant la grossesse, certaines femmes verront une plus grande quantité de poils sur leur corps, et leur chevelure pourra être plus fournie. Quelques mois après l’accouchement, certaines femmes remarqueront une chute de cheveux plus abondante qu’à l’habitude.</h4>
    <h1>Peau</h1>
    <h4>Les variations hormonales pendant la grossesse stimulent la peau et le cuir chevelu et entraînent des changements chez plusieurs femmes. Ces modifications ne doivent pas vous inquiéter. La majorité d’entre elles diminuent ou disparaissent dans les mois suivant la naissance du bébé.</h4>
    <h1>Hyperpigmentation</h1>
    <h4>La peau devient plus foncée chez la majorité des femmes enceintes. On appelle cela l’hyperpigmentation. Cela est surtout situé sur une mince ligne foncée entre le nombril et le pubis. L’hyperpigmentation peut aussi être présente sur les aréoles des seins, le périnée, l’anus, le cou, les aisselles et la peau autour du nombril.

        Le masque de grossesse est une conséquence de l’hyperpigmentation. Il s’agit de marques brunes qui apparaissent sur le visage.

        Le soleil peut aggraver l’hyperpigmentation et le masque de grossesse. Vous pouvez appliquer un écran solaire pour vous en protéger (voir Écran solaire).

        L’hyperpigmentation et le masque de grossesse s’atténuent après l’accouchement, pour disparaître habituellement dans l’année suivant la naissance du bébé.</h4>
    <h1>Vergetures</h1>
    <h4>Des vergetures peuvent apparaître vers la seconde moitié de la grossesse. Elles sont visibles principalement sur le ventre, les seins et les cuisses, mais aussi sous les aisselles, dans le bas du dos, sur les fesses et les bras. Les vergetures se forment lorsqu’une couche profonde de la peau s’étire sous l’effet du corps qui se transforme. Elles ont initialement une teinte rosée ou violacée et deviennent moins apparentes au fil du temps.

        Il n’y a pas de moyen reconnu comme efficace pour prévenir ou traiter les vergetures. Le mouvement de massage qui accompagne l’application d’une crème hydratante pourrait aider légèrement à atténuer leur présence, mais les crèmes hydratantes elles-mêmes auraient peu d’effet.

    </h4> 
